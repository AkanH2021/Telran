package com.company;

public class ForCycle {


    public static void main(String[] args) {
        System.out.println("-------for");
        for (int i = 1; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("----- while");
        int i = 1;
        while (i < 10) {
            System.out.println(i);
            i++;
        }
    }
}
